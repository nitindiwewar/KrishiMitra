export function useTranslation() {
  const [lang, setLang] = useState(localStorage.getItem('krishimitra_lang') || 'en');

  useEffect(() => {
    const handleLangChange = () => {
      setLang(localStorage.getItem('krishimitra_lang') || 'en');
    };
    window.addEventListener('krishimitra_lang_changed', handleLangChange);
    return () => {
      window.removeEventListener('krishimitra_lang_changed', handleLangChange);
    };
  }, []);

  const t = (key: string): string => {
    const dict = translations[lang] || translations.en;
    return dict[key] || translations.en[key] || key;
  };

  return { t, lang, setLang };
}

